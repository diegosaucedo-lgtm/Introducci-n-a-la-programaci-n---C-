
//Operadores matemáticos

#include<iostream>

using namespace std;

int main() {
	 
	int num1, num2, suma = 0, resta = 0, multiplicacion = 0, division = 0;
	
	cout<<"Operaciones matemáticas"<<endl;
	cout<<"Digita el primer número: "<<endl; cin>>num1;
	cout<<"Digita el segundo número: "<<endl; cin>>num2;
	
	suma=num1+num2;
	resta=num1-num2;
	multiplicacion=num1*num2;
	division=num1/num2;
	
	cout<<"Operaciones"<<endl;
	cout<<"Suma: "<<suma<<endl;
	cout<<"Resta: "<<resta<<endl;
	cout<<"multiplicación: "<<multiplicacion<<endl;
	cout<<"División: "<<division;

	return 0;
}























